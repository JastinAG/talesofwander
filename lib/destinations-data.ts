export const getDestinationById = (id: string) => {
  return destinations.find((destination) => destination.id === id)
}

export const getDestinationsByCategory = (category: string) => {
  return destinations.filter((destination) => destination.category === category)
}

export const getBeachDestinations = () => {
  return getDestinationsByCategory("beach")
}

export const getSafariDestinations = () => {
  return getDestinationsByCategory("safari")
}

export const getAdventureDestinations = () => {
  return getDestinationsByCategory("adventure")
}

export const getCulturalDestinations = () => {
  return getDestinationsByCategory("cultural")
}
